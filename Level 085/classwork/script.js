let choice = prompt("1 - Americano, 2 - Latte, 3 - Cappuccino: ");


switch (choice) {
    case "1":
        console.log("Serve Americano");
        break;
    case "2":
        console.log("Serve Latte");
        break;
    case "3":
        console.log("Serve Cappuccino");
        break;
    default:
        console.log("Unknown");
        

}