# Comparison of two types of division

# Regular division (/)
result1 = 10 / 3
print("10 / 3 =", result1)

# Integer division (//)
result2 = 10 // 3
print("10 // 3 =", result2)

# Explanation of the difference

# Difference:
# 1. The / operator performs regular division and returns a floating-point number (float).
# 2. The // operator performs integer division and returns only the whole part (integer).


